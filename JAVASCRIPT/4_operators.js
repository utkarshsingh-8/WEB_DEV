console.log('Operators in JS')

let a = 10;
let b = 4;

console.log("a + b =", a + b)
console.log("a - b =", a - b)
console.log("a * b =", a * b)
console.log("a / b =", a / b)
console.log("a % b =", a % b)
console.log("a ** b =", a ** b)

// COMPARISON OPERATOR
let comp1 = 6
let comp2 = "6"
console.log("comp1 == comp2 ", comp1 == comp2)
console.log("comp1 != comp2 ", comp1 != comp2)
// (== and != ) both compares only values not their data type 
console.log("comp1 === comp2 ", comp1 === comp2)
console.log("comp1 !== comp2 ", comp1 !== comp2)
// (=== and !== ) both compares only values not their data type 
